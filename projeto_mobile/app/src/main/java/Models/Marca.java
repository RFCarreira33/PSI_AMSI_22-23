package Models;

import java.util.concurrent.atomic.AtomicInteger;

public class Marca {
    int id;
    String nome;

    public Marca( int id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
